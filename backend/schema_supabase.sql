-- 1. TABELA DE ARQUIVOS IMPORTADOS
CREATE TABLE ceu_arquivos_importados (
    id_codigo BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_uuid UUID DEFAULT gen_random_uuid() NOT NULL UNIQUE,
    nome_arquivo TEXT NOT NULL,
    conta TEXT NOT NULL,
    mes_ano TEXT NOT NULL,
    saldo_inicial NUMERIC(15,2) DEFAULT 0.00,
    total_debitos NUMERIC(15,2) DEFAULT 0.00,
    total_creditos NUMERIC(15,2) DEFAULT 0.00,
    saldo_final NUMERIC(15,2) DEFAULT 0.00,
    data_importacao TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. TABELA DE DEPARTAMENTOS
CREATE TABLE ceu_departamentos (
    id_codigo BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_uuid UUID DEFAULT gen_random_uuid() NOT NULL UNIQUE,
    nome TEXT NOT NULL,
    tipo TEXT CHECK (tipo IN ('credito', 'debito', 'misto')) NOT NULL,
    ativo BOOLEAN DEFAULT TRUE NOT NULL
);

