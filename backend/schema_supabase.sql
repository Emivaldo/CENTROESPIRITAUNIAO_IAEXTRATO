-- 3. TABELA DE LANÇAMENTOS (Extrato detalhado)
CREATE TABLE ceu_lancamentos (
    id_codigo BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    id_uuid UUID DEFAULT gen_random_uuid() NOT NULL UNIQUE,
    
    id_arquivo_origem BIGINT NOT NULL,
    id_departamento BIGINT,
    
    status TEXT CHECK (status IN ('pendente', 'conciliado', 'finalizado')) DEFAULT 'pendente' NOT NULL,
    data_transacao DATE NOT NULL,
    historico_banco TEXT NOT NULL,
    detalhes_complementares TEXT,
    tipo_movimento TEXT CHECK (tipo_movimento IN ('Receita', 'Despesa')) NOT NULL,
    valor_absoluto NUMERIC(15,2) NOT NULL,
    
    criado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    atualizado_em TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    
    CONSTRAINT fk_arquivo
      FOREIGN KEY(id_arquivo_origem) 
      REFERENCES ceu_arquivos_importados(id_codigo)
      ON DELETE CASCADE,
      
    CONSTRAINT fk_departamento
      FOREIGN KEY(id_departamento) 
      REFERENCES ceu_departamentos(id_codigo)
      ON DELETE SET NULL
);

-- Ativa políticas de RLS para o Supabase (Segurança de acesso por API)
ALTER TABLE ceu_arquivos_importados ENABLE ROW LEVEL SECURITY;
ALTER TABLE ceu_departamentos ENABLE ROW LEVEL SECURITY;
ALTER TABLE ceu_lancamentos ENABLE ROW LEVEL SECURITY;

-- Permite leitura e escrita anônima para testes/desenvolvimento
CREATE POLICY "Public Access" ON ceu_arquivos_importados FOR ALL USING (true);
CREATE POLICY "Public Access" ON ceu_departamentos FOR ALL USING (true);
CREATE POLICY "Public Access" ON ceu_lancamentos FOR ALL USING (true);
